/*============================================================
 Procedure 1 : Get Customer Revenue Report
 Purpose     : Display total revenue generated by each customer.
============================================================*/

delimiter $$ 
create procedure GetCustomerRevenue()
begin 
	select customername  , round(sum(totalamount),2) as total_revenue from customers 
    inner join orders using(customerkey) 
    inner join orderdetails using(orderid) 
    group by 1 
    order by total_revenue desc ;
    
end $$
delimiter ;

call GetCustomerRevenue();

/*============================================================
 Procedure 2 : Get Top 10 Customers
 Purpose     : Display top 10 customers by revenue.
============================================================*/ 

delimiter $$ 
create procedure GetTopCustomers(in top_n int)
begin 
	select customername , round(sum(totalamount),2) as total_revenue
    from customers inner join orders using(customerkey) 
    inner join orderdetails using(orderid)
    group by 1 
    order by total_revenue desc limit top_n;
    
end $$ 
delimiter ;

call GetTopCustomers(15);



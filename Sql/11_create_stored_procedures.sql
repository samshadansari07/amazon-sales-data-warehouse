/*============================================================
 Procedure 5 : Monthly Revenue Report
 Purpose     : Display monthly revenue summary.
============================================================*/

delimiter $$ 
create procedure monthly_revenue()
begin 
	select monthname(orderdate) as months , round(sum(totalamount),2) as total_revenue
    from orders inner join orderdetails using(orderid) 
    group by 1 
